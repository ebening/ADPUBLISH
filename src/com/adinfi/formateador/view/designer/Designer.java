    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (deleteModuleId > 0) {
            int result = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar el módulo?.", Utilerias.getProperty(ApplicationProperties.APP_TITLE), JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                TemplateDAO tempDAO = new TemplateDAO();
                int val = tempDAO.validaEliminacionModulo(deleteModuleId);
                boolean res = val != TemplateDAO.DELETE_TEMPLATE;
                if (res) {
                    if (val == TemplateDAO.USED_TEMPLATE) {
                        JOptionPane.showMessageDialog(this, "El módulo no se puede eliminar, ya que está siendo usado por un documento.");
                    } else if (val == TemplateDAO.INTO_TEMPLATE) {
                        JOptionPane.showMessageDialog(this, "El módulo no se puede eliminar, ya que está siendo usado por una plantilla.");
                    }
                    deleteModuleId = 0;
                } else {
                    res = tempDAO.removeModule(deleteModuleId);
                    if (!res) {
                        JOptionPane.showMessageDialog(this, "Ocurrió un error al eliminar.");
                    } else {
                        JOptionPane.showMessageDialog(this, "El módulo se eliminó satisfactoriamente.");
                        firstPage.setEnabled(true);
                        deleteModuleId = 0;
                        nameModuleSearch = "";
                        jButton3.setEnabled(false);
                        jButton5.setVisible(false);
                        jButton1.setVisible(true);
                        nameModuleSearch = jTextField2.getText();
                        populatePanel();
                        HEIGHT_MODEL = HEIGHT_MIN;
                        //btnPrev.setVisible(false);
                        createTemplate();
                        previewPanel.removeAll();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un módulo para eliminar.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

