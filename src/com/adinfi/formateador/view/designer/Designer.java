    public void loadPageToCompleteTemplate() {

        if (pxToComplete > 0) {
            HEIGHT_MODEL = (int) Math.round(pxToComplete * 25.4 / 10 / DPI);;
        } else {
            if (isFirstPage) {
                HEIGHT_MODEL = HEIGHT_MAX_FP;
            } else {
                HEIGHT_MODEL = HEIGHT_MAX;
            }

        }
        createTemplate();
    }

